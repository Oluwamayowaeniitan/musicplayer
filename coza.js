//CLICKABLE IMAGE
var icon = document.querySelectorAll('.icon');

//SONGS
var song = document.querySelectorAll('#son');

icon[0].onclick = function(){
    if(song[0].paused){
        song[0].play();
        icon[0].src = 'pause.png';
    }
    else{
        song[0].pause();
        icon[0].src = 'cozaa.jpg';
    }
}
 
